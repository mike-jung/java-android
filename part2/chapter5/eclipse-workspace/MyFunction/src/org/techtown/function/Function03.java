package org.techtown.function;

public class Function03 {

	public static void main(String[] args) {
	    int count = 0;
		
	    for (int i = 1; i < 11; i++) {
	      count = count + i;
	    }
		
	    System.out.println(count);

	    count = 0;
		
	    for (int i = 1; i < 101; i++) {
	      count = count + i;
	    }
		
	    System.out.println(count);

	}

}
